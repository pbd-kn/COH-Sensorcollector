        CURLOPT_COOKIEJAR => $cookieFile,
        CURLOPT_TIMEOUT => 15,
        CURLOPT_FOLLOWLOCATION => false,
        CURLOPT_HEADER => true,
        CURLOPT_HTTPHEADER => ['Accept: application/json,text/plain,text/html,*/*', 'Connection: close'],
    ]);

    $response = curl_exec($ch);
    if ($response === false) {
        $error = curl_error($ch);
        curl_close($ch);
        throw new RuntimeException('cURL error: ' . $error);
    }

    $code = (int)curl_getinfo($ch, CURLINFO_HTTP_CODE);
    $type = (string)curl_getinfo($ch, CURLINFO_CONTENT_TYPE);
    $headerSize = (int)curl_getinfo($ch, CURLINFO_HEADER_SIZE);
    curl_close($ch);

    $headers = substr((string)$response, 0, $headerSize);
    $body = substr((string)$response, $headerSize);

    return [
        'url' => $url,
        'code' => $code,
        'type' => $type,
        'headers' => $headers,
        'body' => $body,
    ];
}

function printPathResult(string $path, array $result): void
{
    echo $path . ' -> HTTP ' . $result['code'];
    if ($result['type'] !== '') {
        echo ' ' . $result['type'];
    }
    if (preg_match('/^location:\s*(.+)$/mi', (string)$result['headers'], $matches)) {
        echo ' Location: ' . trim($matches[1]);
    }
    echo PHP_EOL;

    $body = trim((string)$result['body']);
    if ($body === '') {
        echo '<leer>' . PHP_EOL;
        return;
    }

    if (str_contains(strtolower((string)$result['type']), 'javascript') || preg_match('/\.m?js(?:\?|$)/i', $path)) {
        printScriptHints($body);
        return;
    }

    $decoded = json_decode($body, true);
    if (json_last_error() === JSON_ERROR_NONE) {
        echo json_encode($decoded, JSON_PRETTY_PRINT | JSON_UNESCAPED_UNICODE | JSON_UNESCAPED_SLASHES) . PHP_EOL;
        return;
    }

    if (strlen($body) > 4000) {
        $body = substr($body, 0, 4000) . PHP_EOL . '... gekuerzt ...';
    }
    echo $body . PHP_EOL;
}

function printScriptHints(string $body): void
{
    $hints = collectApiLikeStrings($body);
    $methodHints = collectHttpMethodHints($body);

    if (!$hints && !$methodHints) {
        echo 'Keine API-Hinweise in dieser JavaScript-Datei gefunden.' . PHP_EOL;
        return;
    }

    if ($methodHints) {
        echo 'HTTP-Aufrufe/Pfade:' . PHP_EOL;
        foreach (array_slice($methodHints, 0, 120) as $hint) {
            echo '  ' . $hint . PHP_EOL;
        }
    }

    if ($hints) {
        echo 'Weitere Pfad-Hinweise:' . PHP_EOL;
        foreach (array_slice($hints, 0, 160) as $hint) {
            echo '  ' . $hint . PHP_EOL;
        }
    }
}

function collectHttpMethodHints(string $text): array
{
    $values = [];

    preg_match_all('/httpClient\.request\("([^"]+)",`\$\{this\.configuration\.basePath\}([^`]+)`/i', $text, $matches, PREG_SET_ORDER);
    foreach ($matches as $match) {
        $values[strtoupper($match[1]) . ' ' . $match[2]] = true;
    }

    preg_match_all('/httpClient\.request\("([^"]+)","([^"]+)"/i', $text, $matches, PREG_SET_ORDER);
    foreach ($matches as $match) {
        $values[strtoupper($match[1]) . ' ' . $match[2]] = true;
    }

    preg_match_all('/\.(get|post|put|delete|patch)\(`\$\{this\.configuration\.basePath\}([^`]+)`/i', $text, $matches, PREG_SET_ORDER);
    foreach ($matches as $match) {
        $values[strtoupper($match[1]) . ' ' . $match[2]] = true;
    }

    return array_keys($values);
}

function executeJsFind(string $baseUrl, string $needle, string $cookieFile): void
{
    $needle = trim($needle);
    if ($needle === '') {
        echo 'WARNUNG: Suchtext fehlt, Beispiel: jsfind soc' . PHP_EOL;
        return;
    }

    echo "Suche in EM.setup JavaScript nach: $needle" . PHP_EOL;
    $assets = collectEmSetupAssets($baseUrl, $cookieFile);
    if (!$assets) {
        echo 'WARNUNG: Keine JavaScript-Dateien gefunden.' . PHP_EOL;
        return;
    }

    $totalHits = 0;
    foreach ($assets as $asset) {
        if (!preg_match('/\.m?js(?:\?|$)/i', $asset)) {
            continue;
        }

        $assetUrl = str_starts_with($asset, 'http') ? $asset : $baseUrl . '/' . ltrim($asset, '/');
        $res = curlRaw($assetUrl, $cookieFile);
        if (!$res['ok']) {
            continue;
        }

        $hits = findTextContexts($res['body'], $needle, 180, 8);
        if (!$hits) {
            continue;
        }

        echo PHP_EOL . "Asset $asset" . PHP_EOL;
        foreach ($hits as $hit) {
            echo '  ...' . $hit . '...' . PHP_EOL;
        }
        $totalHits += count($hits);
    }

    if ($totalHits === 0) {
        echo 'Keine Treffer gefunden.' . PHP_EOL;
    } else {
        echo PHP_EOL . "Treffer gesamt: $totalHits" . PHP_EOL;
    }
}

function collectEmSetupAssets(string $baseUrl, string $cookieFile): array
{
    $startPaths = [
        '/em-setup/',
        '/em-setup/dashboard',
        '/em-setup/hems-configurator/devices',
    ];
    $assets = [
        '/kiwios-components/app-frame.js' => true,
    ];

    foreach ($startPaths as $path) {
        $res = curlRaw($baseUrl . $path, $cookieFile);
        if (!$res['ok']) {
            continue;
        }
        foreach (extractAssetPaths($res['body']) as $asset) {
            $assets[resolveAssetPath($path, $asset)] = true;
        }
    }

    $seen = [];
    $queue = array_keys($assets);
    while ($queue && count($seen) < 80) {
        $asset = array_shift($queue);
        if (isset($seen[$asset])) {
            continue;
        }
        $seen[$asset] = true;

        if (!preg_match('/\.m?js(?:\?|$)/i', $asset)) {
            continue;
        }

        $assetUrl = str_starts_with($asset, 'http') ? $asset : $baseUrl . '/' . ltrim($asset, '/');
        $res = curlRaw($assetUrl, $cookieFile);
        if (!$res['ok']) {
            continue;
        }

        foreach (extractAssetPaths($res['body']) as $nextAsset) {
            $resolved = resolveAssetPath($asset, $nextAsset);
            if (!isset($seen[$resolved])) {
                $queue[] = $resolved;
            }
        }
    }

    return array_keys($seen);
}

function findTextContexts(string $text, string $needle, int $contextLength, int $limit): array
{
    $hits = [];
    $needleLength = strlen($needle);

    if ($needleLength <= 3) {
        $pattern = '/(?<![A-Za-z0-9_])' . preg_quote($needle, '/') . '(?![A-Za-z0-9_])/i';
        preg_match_all($pattern, $text, $matches, PREG_OFFSET_CAPTURE);
        $positions = array_map(static fn(array $match): int => $match[1], $matches[0] ?? []);
    } else {
        $positions = [];
        $offset = 0;
        while (($pos = stripos($text, $needle, $offset)) !== false) {
            $positions[] = $pos;
            $offset = $pos + max(1, $needleLength);
            if (count($positions) >= $limit) {
                break;
            }
        }
    }

    foreach ($positions as $pos) {
        $start = max(0, $pos - $contextLength);
        $length = $needleLength + ($contextLength * 2);
        $snippet = substr($text, $start, $length);
        $snippet = preg_replace('/\s+/', ' ', $snippet) ?? $snippet;
        if (strlen($snippet) > 420) {
            $snippet = substr($snippet, 0, 420);
        }
        $hits[] = $snippet;
        if (count($hits) >= $limit) {
            break;
        }
    }

    return $hits;
}

function probeEndpoint(string $baseUrl, string $path, string $username, string $password, string $cookieFile): array
{
    @unlink($cookieFile);
    try {
        ampereLogin($baseUrl, $username, $password, $cookieFile);
    } catch (Throwable $e) {
        return [
            'path' => $path,
            'code' => 'login: ' . $e->getMessage(),
            'type' => '',
            'body' => '',
        ];
    }

    $url = $baseUrl . $path;
    $ch = curl_init($url);
    curl_setopt_array($ch, [
        CURLOPT_RETURNTRANSFER => true,
        CURLOPT_COOKIEFILE => $cookieFile,
        CURLOPT_TIMEOUT => 8,
        CURLOPT_FOLLOWLOCATION => false,
        CURLOPT_HEADER => true,
        CURLOPT_HTTPHEADER => ['Accept: application/json,text/plain,*/*', 'Connection: close'],
    ]);

    $response = curl_exec($ch);
    if ($response === false) {
        $error = curl_error($ch);
        curl_close($ch);
        return [
            'path' => $path,
            'code' => 'curl: ' . $error,
            'type' => '',
            'body' => '',
        ];
    }

    $code = curl_getinfo($ch, CURLINFO_HTTP_CODE);
    $headerSize = (int)curl_getinfo($ch, CURLINFO_HEADER_SIZE);
    $type = (string)curl_getinfo($ch, CURLINFO_CONTENT_TYPE);
    curl_close($ch);
    $headers = substr((string)$response, 0, $headerSize);
    $response = substr((string)$response, $headerSize);

    $body = preg_replace('/\s+/', ' ', trim((string)$response)) ?? '';
    if (strlen($body) > 120) {
        $body = substr($body, 0, 120) . '...';
    }
    if (preg_match('/^location:\s*(.+)$/mi', $headers, $matches)) {
        $body = 'Location: ' . trim($matches[1]) . ($body !== '' ? ' | ' . $body : '');
    }

    return [
        'path' => $path,
        'code' => (string)$code,
        'type' => $type,
        'body' => $body,
    ];
}

