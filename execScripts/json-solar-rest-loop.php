            printHelp();
            continue;
        }

        if (in_array(strtolower($filter), ['r', 'reload'], true)) {
            $items = safeLoadItemsRaw($baseUrl, true, $username, $password, $cookieFile, $rawData);
            continue;
        }

        if (in_array(strtolower($filter), ['login', 'relogin'], true)) {
            @unlink($cookieFile);
            try {
                ampereLogin($baseUrl, $username, $password, $cookieFile);
                echo 'Login erledigt.' . PHP_EOL;
            } catch (Throwable $e) {
                echo 'WARNUNG: Login fehlgeschlagen: ' . $e->getMessage() . PHP_EOL;
            }
            continue;
        }

        if (preg_match('/^jsfind\s+(.+)$/i', $filter, $matches)) {
            executeJsFind($baseUrl, trim($matches[1]), $cookieFile);
            continue;
        }

        if (str_starts_with($filter, '/')) {
            executePathCommand($baseUrl, $filter, $username, $password, $cookieFile);
            continue;
        }

        if (in_array(strtolower($filter), ['raw', 'json'], true)) {
            $file = writeRawJsonFile($rawData);
            echo "Raw JSON gespeichert: $file" . PHP_EOL;
            continue;
        }

        printItems($items, $filter);
    }
} catch (Throwable $e) {
    fwrite(STDERR, 'WARNUNG: ' . $e->getMessage() . PHP_EOL);
}
