function normalizeBaseUrl(string $value): string
{
    $value = trim($value);
    if ($value === '') {
        return '';
    }

    if (!preg_match('~^https?://~i', $value)) {
        $value = 'http://' . $value;
    }

    return rtrim($value, '/');
}

function ampereLogin(string $baseUrl, string $username, string $password, string $cookieFile): void
{
    global $showLoginResult;

    $dir = dirname($cookieFile);
    if (!is_dir($dir)) {
        @mkdir($dir, 0770, true);
    }

    $ch = curl_init($baseUrl . '/auth/login');
    curl_setopt_array($ch, [
        CURLOPT_POST => true,
        CURLOPT_POSTFIELDS => http_build_query([
            'username' => $username,
            'password' => $password,
        ]),
        CURLOPT_COOKIEJAR => $cookieFile,
        CURLOPT_COOKIEFILE => $cookieFile,
        CURLOPT_RETURNTRANSFER => true,
        CURLOPT_HEADER => true,
        CURLOPT_TIMEOUT => 10,
        CURLOPT_FOLLOWLOCATION => false,
    ]);

    $response = curl_exec($ch);
    if ($response === false) {
        $error = curl_error($ch);
        curl_close($ch);
        throw new RuntimeException('Login failed: ' . $error);
    }

    $code = curl_getinfo($ch, CURLINFO_HTTP_CODE);
    $headerSize = (int)curl_getinfo($ch, CURLINFO_HEADER_SIZE);
    curl_close($ch);
    $headers = substr((string)$response, 0, $headerSize);
    $body = substr((string)$response, $headerSize);

    if (!is_file($cookieFile) || filesize($cookieFile) === 0) {
        writeCookieJarFromHeaders($baseUrl, $headers, $cookieFile);
    }

    if ($showLoginResult) {
        printLoginResult($baseUrl, $code, $headers, $body, $cookieFile);
    }

    if (!in_array($code, [200, 302, 303], true)) {
        throw new RuntimeException("Login HTTP error $code");
    }
}

function printLoginResult(string $baseUrl, int $code, string $headers, string $response, string $cookieFile): void
{
    $cookieInfo = 'keine Cookie-Datei';
    if (is_file($cookieFile)) {
        $cookieInfo = filesize($cookieFile) . ' Bytes';
        $cookieText = (string)file_get_contents($cookieFile);
        if (preg_match('/\s(kiwisessionid)\s+([^\s]+)/', $cookieText, $matches)) {
            $cookieInfo .= ', ' . $matches[1] . '=' . maskValue($matches[2]);
        }
    }

    $setCookie = '<kein Set-Cookie>';
    if (preg_match('/^set-cookie:\s*(.+)$/mi', $headers, $matches)) {
        $setCookie = maskCookieHeader(trim($matches[1]));
    }

    $body = trim($response);
    if ($body === '') {
        $body = '<leer>';
    } elseif (strlen($body) > 160) {
        $body = substr($body, 0, 160) . '...';
    }

    echo "Login POST $baseUrl/auth/login -> HTTP $code, Set-Cookie: $setCookie, Cookie-Datei: $cookieInfo, Antwort: $body" . PHP_EOL;
}

function maskValue(string $value): string
{
    if (strlen($value) <= 8) {
        return '***';
    }

    return substr($value, 0, 4) . '...' . substr($value, -4);
}

function maskCookieHeader(string $header): string
{
    return preg_replace_callback('/(kiwisessionid=)([^;]+)/', function (array $matches): string {
        return $matches[1] . maskValue($matches[2]);
    }, $header) ?? $header;
}

function writeCookieJarFromHeaders(string $baseUrl, string $headers, string $cookieFile): void
{
    if (!preg_match('/^set-cookie:\s*kiwisessionid=([^;]+)/mi', $headers, $matches)) {
        return;
    }

    $host = parse_url($baseUrl, PHP_URL_HOST) ?: '192.168.178.26';
    $value = trim($matches[1]);
    $expires = time() + 3600;
    if (preg_match('/Max-Age=(\d+)/i', $headers, $maxAge)) {
        $expires = time() + (int)$maxAge[1];
    }

    $dir = dirname($cookieFile);
    if (!is_dir($dir)) {
        @mkdir($dir, 0770, true);
    }

    $content = "# Netscape HTTP Cookie File\n";
    $content .= "# generated by json-solar-rest-loop.php fallback\n\n";
    $content .= "#HttpOnly_$host\tFALSE\t/\tFALSE\t$expires\tkiwisessionid\t$value\n";
    @file_put_contents($cookieFile, $content);
}

function ampereRequest(string $baseUrl, string $path, string $username, string $password, string $cookieFile, bool $retry = false): array
{
    if (!file_exists($cookieFile)) {
        ampereLogin($baseUrl, $username, $password, $cookieFile);
    }

    $ch = curl_init($baseUrl . $path);
    curl_setopt_array($ch, [
        CURLOPT_RETURNTRANSFER => true,
        CURLOPT_COOKIEFILE => $cookieFile,
        CURLOPT_TIMEOUT => 15,
        CURLOPT_FOLLOWLOCATION => false,
        CURLOPT_HTTPHEADER => ['Accept: application/json', 'Connection: close'],
    ]);

    $response = curl_exec($ch);
    if ($response === false) {
        $error = curl_error($ch);
        curl_close($ch);
        throw new RuntimeException('cURL error: ' . $error);
    }

    $code = curl_getinfo($ch, CURLINFO_HTTP_CODE);
    curl_close($ch);

    if (in_array($code, [301, 302, 303, 401, 403], true)) {
        if ($retry) {
            throw new RuntimeException("Auth failed after retry HTTP $code");
        }

        @unlink($cookieFile);
        ampereLogin($baseUrl, $username, $password, $cookieFile);
        return ampereRequest($baseUrl, $path, $username, $password, $cookieFile, true);
    }

    if ($code === 404) {
        if ($retry) {
            throw new RuntimeException("HTTP 404 after login");
        }

        @unlink($cookieFile);
        ampereLogin($baseUrl, $username, $password, $cookieFile);
        return ampereRequest($baseUrl, $path, $username, $password, $cookieFile, true);
    }

    if ($code !== 200) {
        throw new RuntimeException("HTTP error $code");
    }

    return [
        'ok' => true,
        'http_code' => 200,
        'data' => (string)$response,
    ];
}

function loadItemsRaw(string $baseUrl, string $username, string $password, string $cookieFile): array
{
    try {
        $result = ampereRequest($baseUrl, '/rest/items', $username, $password, $cookieFile);
    } catch (Throwable $e) {
        if (!str_contains($e->getMessage(), '404')) {
            throw $e;
        }

        $result = ampereRequest($baseUrl, '/rest/items/', $username, $password, $cookieFile);
    }
    $data = json_decode($result['data'], true);

    if (json_last_error() !== JSON_ERROR_NONE || !is_array($data)) {
        throw new RuntimeException('Invalid JSON response: ' . json_last_error_msg());
    }

    return $data;
}

function loadItemsRawWithFallbacks(string &$baseUrl, bool $baseUrlExplicit, string $username, string $password, string $cookieFile): array
{
    $candidates = [$baseUrl];

    $candidates = array_values(array_unique(array_map('normalizeBaseUrl', $candidates)));
    $lastError = null;
    $firstError = null;

    foreach ($candidates as $candidate) {
        echo "Versuche REST: $candidate/rest/items" . PHP_EOL;
        try {
            $data = loadItemsRaw($candidate, $username, $password, $cookieFile);
            $baseUrl = $candidate;
            return $data;
        } catch (Throwable $e) {
            $lastError = $e;
            if ($firstError === null) {
                $firstError = $e;
            }
            echo "  nicht ok: " . $e->getMessage() . PHP_EOL;
        }
    }

    throw new RuntimeException($firstError ? $firstError->getMessage() : ($lastError ? $lastError->getMessage() : 'kein REST-Endpunkt erreichbar'));
}

