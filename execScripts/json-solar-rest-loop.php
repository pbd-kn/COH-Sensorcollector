function extractAssetPaths(string $html): array
{
    preg_match_all('/(?:src|href)=["\']([^"\']+\.(?:js|mjs|css)(?:\?[^"\']*)?)["\']/i', $html, $matches);
    $assets = $matches[1] ?? [];
    preg_match_all('/["\']([^"\']+\.(?:js|mjs|css)(?:\?[^"\']*)?)["\']/i', $html, $stringMatches);
    foreach ($stringMatches[1] ?? [] as $asset) {
        if (str_contains($asset, '://') || str_starts_with($asset, 'data:')) {
            continue;
        }
        $assets[] = $asset;
    }

    return array_values(array_unique($assets));
}

function resolveAssetPath(string $currentPath, string $asset): string
{
    if (str_starts_with($asset, 'http') || str_starts_with($asset, '/')) {
        return $asset;
    }

    $dir = rtrim(dirname('/' . ltrim($currentPath, '/')), '/');
    if ($dir === '') {
        $dir = '/';
    }

    return rtrim($dir, '/') . '/' . ltrim($asset, '/');
}

function collectApiLikeStrings(string $text): array
{
    preg_match_all('~["\']((?:/|https?://)[^"\']*(?:api|rest|devices|saj|battery|inverter|meter|hems|em-setup)[^"\']*)["\']~i', $text, $matches);
    $values = [];
    foreach ($matches[1] ?? [] as $value) {
        $value = stripcslashes($value);
        if (strlen($value) > 180) {
            $value = substr($value, 0, 180) . '...';
        }
        $values[$value] = true;
    }

    preg_match_all('#["\'](/(?:[A-Za-z0-9_.~:@!$&()*+,;=/-]+)(?:\?[A-Za-z0-9_.~:@!$&()*+,;=/%-]+)?)["\']#', $text, $pathMatches);
    foreach ($pathMatches[1] ?? [] as $value) {
        if (str_starts_with($value, '/assets/') || str_starts_with($value, '/static/')) {
            continue;
        }
        if (strlen($value) < 4 || strlen($value) > 180) {
            continue;
        }
        $values[$value] = true;
    }

    return array_keys($values);
}

function printApiLikeStrings(string $text): void
{
    foreach (array_slice(collectApiLikeStrings($text), 0, 25) as $hint) {
        echo "  Hinweis: $hint" . PHP_EOL;
    }
}

function normalizeItems(array $data): array
{
    if (isset($data['items']) && is_array($data['items'])) {
        return $data['items'];
    }

    return $data;
}

function normalizeUnit(string $unit): string
{
    $unit = trim($unit);
    $unit = @iconv('UTF-8', 'UTF-8//IGNORE', $unit);
    $unit = str_replace(["\xEF\xBB\xBF", 'Â'], '', $unit);
    return strtolower(trim($unit));
}

function convertState($state): array
{
    $rawState = $state;
    if ($state === null || $state === 'NULL' || $state === 'UNDEF') {
        return [
            'valid' => false,
            'rawState' => $rawState,
            'timestampMs' => '',
            'timestampUnix' => '',
            'datetime' => '',
            'rawValue' => '',
            'rawUnit' => '',
            'value' => '',
            'unit' => '',
        ];
    }

    $stateText = trim((string)$state);
    $timestampMs = '';
    $timestampUnix = '';
    $datetime = '';
    $valuePart = $stateText;

    if (str_contains($stateText, '|')) {
        [$maybeTimestamp, $rest] = explode('|', $stateText, 2);
        if (ctype_digit($maybeTimestamp)) {
            $timestampMs = $maybeTimestamp;
            $seconds = (int)floor(((int)$timestampMs) / 1000);
            $timestampUnix = (string)$seconds;
            $datetime = date('Y-m-d H:i:s', $seconds);
            $valuePart = trim($rest);
        }
    }

    $rawValue = $valuePart;
    $rawUnit = '';
    $value = $valuePart;
    $unit = '';

    if (preg_match('/^\s*([+-]?[0-9]+(?:[.,][0-9]+)?(?:[eE][+-]?[0-9]+)?)\s*(.*?)\s*$/u', $valuePart, $m)) {
        $rawValue = $m[1];
        $rawUnit = $m[2] ?? '';
        $numeric = (float)str_replace(',', '.', $rawValue);
        $unitNormalized = normalizeUnit($rawUnit);

        switch ($unitNormalized) {
            case 'w':
                $value = round($numeric / 1000, 4);
                $unit = 'kW';
                break;
            case 'kw':
                $value = round($numeric, 4);
                $unit = 'kW';
                break;
            case 'wh':
                $value = round($numeric / 1000, 4);
                $unit = 'kWh';
                break;
            case 'ws':
                $value = round($numeric / 3600000, 4);
                $unit = 'kWh';
                break;
            case 'kwh':
                $value = round($numeric, 4);
                $unit = 'kWh';
                break;
            case '%':
                $value = round($numeric, 4);
                $unit = '%';
                break;
            case 'a':
                $value = round($numeric, 4);
                $unit = 'A';
                break;
            case 'v':
                $value = round($numeric, 4);
                $unit = 'V';
                break;
            case 'c':
            case '°c':
                $value = round($numeric, 4);
                $unit = 'C';
                break;
            default:
                $value = round($numeric, 4);
                $unit = $rawUnit;
                break;
        }
    }

    return [
        'valid' => true,
        'rawState' => $rawState,
        'timestampMs' => $timestampMs,
        'timestampUnix' => $timestampUnix,
        'datetime' => $datetime,
        'rawValue' => $rawValue,
        'rawUnit' => $rawUnit,
        'value' => $value,
        'unit' => $unit,
    ];
}

function nameShort(string $name): string
{
    $name = preg_replace('/^sajhybrid_/', '', $name) ?? $name;
    $name = preg_replace('/HSR[0-9A-Z]+_/', '', $name) ?? $name;
    return $name;
}

function likeToRegex(string $pattern): string
{
    $regex = preg_quote($pattern, '/');
    $regex = str_replace('%', '.*', $regex);
    return '/^' . $regex . '$/i';
}

function itemMatches(array $item, string $filter): bool
{
    $filter = trim($filter);
    if ($filter === '') {
        return true;
    }

    $haystacks = [
        (string)($item['name'] ?? ''),
        (string)($item['label'] ?? ''),
        (string)($item['link'] ?? ''),
        (string)($item['state'] ?? ''),
        (string)($item['type'] ?? ''),
    ];

    if (str_contains($filter, '%')) {
        $regex = likeToRegex($filter);
        foreach ($haystacks as $text) {
            if (preg_match($regex, $text)) {
                return true;
            }
        }
        return false;
    }

    foreach ($haystacks as $text) {
        if (strcasecmp($text, $filter) === 0 || stripos($text, $filter) !== false) {
            return true;
        }
    }

    return false;
}

function formatNullable($value): string
{
    if ($value === null) {
        return 'NULL';
    }

    return (string)$value;
}

